        <article class="card" id="ui-ux">
          <h2>UI/UX Designer</h2>
		  <img src="images/ui-ux.jpg" alt="ui-ux" class="content-image" />
          <p>
            Seorang UI/UX Developer, atau disebut juga UI/UX Designer atau UI/UX Engineer, adalah seorang profesional di bidang desain antarmuka pengguna (UI) dan pengalaman pengguna (UX) yang bertanggung jawab untuk merancang dan mengembangkan pengalaman interaktif yang optimal bagi pengguna suatu produk atau layanan digital.
			<br>Tugas seorang UI/UX Developer meliputi: <br />
          </p>
          <ul>
            <li>Riset Pengguna: Melakukan penelitian dan menganalisis kebutuhan, preferensi, dan perilaku pengguna potensial untuk mendapatkan pemahaman yang mendalam tentang audiens target produk atau layanan digital.</li>
            <li>Perancangan Antarmuka Pengguna (UI Design): Membuat desain visual antarmuka pengguna yang menarik dan estetis, termasuk pemilihan warna, tipografi, ikon, dan tata letak yang efektif. Desain ini harus memperhatikan prinsip desain UI dan gaya visual yang konsisten dengan merek atau produk yang bersangkutan.</li>
            <li>Perancangan Pengalaman Pengguna (UX Design): Merancang alur kerja (workflow) dan struktur navigasi yang intuitif untuk memastikan pengalaman pengguna yang lancar dan mudah digunakan. Ini melibatkan membuat wireframe, storyboard, dan prototipe interaktif yang menggambarkan alur interaksi pengguna dengan produk.</li>
            <li>Pengembangan Antarmuka Pengguna (UI Development): Mengimplementasikan desain antarmuka pengguna ke dalam kode-kode web atau aplikasi. Biasanya, seorang UI/UX Developer memiliki pengetahuan dan keahlian dalam teknologi front-end seperti HTML, CSS, dan JavaScript, serta mampu menggunakan alat-alat desain dan pengembangan terkait seperti Adobe XD, Sketch, Figma, atau InVision.</li>
			<li>Pengujian dan Iterasi: Melakukan pengujian pengguna (user testing) untuk mengumpulkan umpan balik dan melihat bagaimana pengguna berinteraksi dengan produk. Berdasarkan hasil pengujian, melakukan iterasi dan perbaikan desain untuk meningkatkan pengalaman pengguna.</li>
			<li>Kolaborasi Tim: Bekerja sama dengan tim pengembangan lainnya, termasuk Front End Developer, Backend Developer, dan Manajer Produk, untuk memastikan keselarasan antara desain dan implementasi teknis.</li>
			<li>Memperbarui dan Memelihara: Mengikuti perkembangan tren dan teknologi terbaru dalam desain UI/UX, serta memperbarui dan memelihara desain produk secara kontinu untuk tetap relevan dan memenuhi kebutuhan pengguna yang terus berkembang.</li>
          </ul>
          <p>
            UI/UX Developer harus memiliki pemahaman yang mendalam tentang prinsip desain UI/UX, pengetahuan tentang perilaku pengguna, kreativitas dalam 
			merancang solusi yang inovatif, serta kemampuan untuk berkomunikasi dan berkolaborasi dengan baik dalam tim pengembangan.
          </p>

          <section>
            <h3>Hard skill</h3>
            <img src="images/hardskill.jpg" alt="hardskill" class="content-image" />
            <p>
              Kemampuan yang berkaitan dengan teknis dan bisa menunjang pekerjaan sebagai UI/UX designer. Deretan skill-nya meliputi:  <br />
			  <li>Critical thinking kemampuan berpikir rasional untuk menilai apakah keputusan atau informasi tersebut logis dan sudah tepat</li>
			  <li>Analytical thinking yaitu melihat segala hal dengan lebih rasional dan memudahkan dalam mengevaluasi serta menganalisis kebenaran suatu informasi</li>
			  <li>Problem solving yakni kemampuan untuk menyelesaikan masalah juga dibutuhkan oleh UI/UX dalam proses menciptakan produk</li>
			  <li>Product strategy, dalam membuat produk, UI/UX harus memiliki kemampuan membuat strategi. Dengan apa dan bagaimana cara sebuah produk dibuat</li>
			  <li>Research, kemampuan riset yang baik sangat berguna untuk menunjang pekerjaan sebagai UI/UX karena Anda harus memahami kebutuhan dan ekspektasi user terhadap produk maupun layanan. Riset bisa dilalui dengan wawancara, survei, dan kuesioner. </li>
			  <li>Desain, UI/UX memerlukan skill desain karena desain inilah yang digunakan untuk berkomunikasi dan menyampaikan pesan bagi customer. </li>
            </p>
          </section>

          <section>
            <h3>Softskill </h3>
            <img src="images/softskill.jpg" alt="softskill" class="content-image" />
            <p>
             Keahlian di luar teknis juga dibutuhkan untuk menunjang pekerjaan diantaranya empati, komunikasi, kerja sama, empati, dan manajemen project. </p>
			 <p>
			 Kemampuan komunikasi yang baik UX/UI designer berguna untuk merepresentasikan blueprint dan prototype produk yang sudah dirancang.</p>
			 <p>
			 Sebagaimana diketahui, UI/UX tidak bisa bekerja sendiri karena melibatkan beberapa divisi lain. Oleh karena itu, communication skill dan teamwork sangat memudahkan proses kerja. </p>
            </p>
