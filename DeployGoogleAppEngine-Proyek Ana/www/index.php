        <article class="card" id="front-end">
          <h2>Front End Developer</h2>
          <p>
            Seorang Front End Developer adalah seorang profesional di bidang pengembangan perangkat lunak yang bertanggung jawab untuk merancang, mengembangkan, dan memelihara bagian depan (front end) suatu aplikasi atau website. Mereka fokus pada aspek pengalaman pengguna (user experience) dan interaksi antarmuka pengguna. <br>
			<br> Tugas seorang Front End Developer meliputi: <br/>
			1. Mengimplementasikan desain yang telah dibuat oleh UI/UX Designer ke dalam kode-kode web atau aplikasi. <br>
			2. Mengembangkan fitur-fitur interaktif yang meningkatkan pengalaman pengguna, seperti animasi, validasi form, dan navigasi yang responsif.<br>
			3. Menyelaraskan desain dan fungsionalitas antarmuka pengguna dengan memastikan tampilan dan perilaku yang konsisten di berbagai perangkat dan browser yang berbeda. <br>
			4. Mengoptimalkan performa dan kecepatan loading halaman dengan menggunakan teknik-teknik pengkodean yang efisien. <br>
			5. Menyelidiki dan memperbaiki bug atau masalah yang terjadi pada bagian depan aplikasi atau website. <br>
			6. Berkolaborasi dengan tim pengembangan lain, seperti Backend Developer dan UI/UX Designer, untuk memastikan keselarasan dalam pengembangan produk. <br>
			7. Mempelajari dan menerapkan teknologi dan tren terbaru dalam pengembangan web untuk meningkatkan kualitas dan kecanggihan produk. <br>
			<br> Front End Developer biasanya memiliki pengetahuan mendalam tentang bahasa pemrograman web seperti HTML, CSS, dan JavaScript, serta kerangka kerja (framework) dan library terkait seperti React, Angular, atau Vue.js. Mereka juga harus memahami konsep desain antarmuka pengguna (UI) dan pengalaman pengguna (UX) untuk menciptakan antarmuka yang menarik dan mudah digunakan. <br/>
          </p>
          <section>
            <h3>HTML (Hypertext Markup Language)</h3>
            <img src="images/html.jpg" alt="HTML" class="content-image" />
            <p>
              HTML (Hypertext Markup Language) adalah bahasa markup yang digunakan untuk membuat struktur dan konten dari sebuah halaman web. HTML mengatur tampilan dan konten dari sebuah halaman web melalui tag atau elemen HTML. Setiap tag
              HTML memiliki sintaks atau format yang khusus dan masing-masing memiliki fungsi dan tujuan yang berbeda. Beberapa contoh tag HTML yang sering digunakan antara lain:
            </p>
            <ul>
              <li>&lt;html&gt;: tag pembuka untuk halaman web</li>
              <li>&lt;head&gt; : tag yang berisi informasi tentang halaman web, seperti judul halaman, meta tag, dan script</li>
              <li>&lt;body&gt; : tag yang berisi konten dari halaman web</li>
              <li>&lt;h1&gt; hingga &lt;h6&gt; : tag untuk membuat judul atau heading</li>
              <li>&lt;p&gt; : tag untuk membuat paragraf</li>
              <li>&lt;a&gt; : tag untuk membuat hyperlink atau tautan</li>
              <li>&lt;img&gt; : tag untuk menampilkan gambar</li>
              <li>&lt;ul&gt; dan &lt;li&gt; : tag untuk membuat daftar dengan bullet point</li>
              <li>&lt;table&gt; dan &lt;tr&gt; : tag untuk membuat tabel dan baris tabel</li>
            </ul>
            <p>
              HTML biasanya ditulis dengan menggunakan text editor atau Integrated Development Environment (IDE), kemudian disimpan dengan ekstensi file .html. Setelah halaman web selesai dibuat, halaman tersebut dapat diakses melalui
              browser web seperti Google Chrome, Mozilla Firefox, atau Microsoft Edge.
            </p>
          </section>
          <section>
            <h3>CSS (Cascading Style Sheets)</h3>
            <p>
              CSS (Cascading Style Sheets) adalah bahasa stylesheet yang digunakan untuk mengontrol tampilan dan tata letak dari dokumen HTML dan XML. Dengan CSS, Anda dapat mengubah warna teks, font, margin, padding, posisi, dan banyak
              lagi dalam dokumen HTML dan XML. CSS menggunakan aturan-aturan untuk menghubungkan elemen HTML atau XML dengan gaya tertentu. Aturan CSS terdiri dari selektor dan deklarasi. Selektor adalah bagian dari aturan CSS yang
              menentukan elemen HTML atau XML mana yang akan diberikan gaya. Deklarasi terdiri dari sifat dan nilai. Sifat adalah aspek yang akan diubah dalam elemen HTML atau XML, seperti warna teks atau margin, sedangkan nilai adalah
              nilai yang diberikan pada sifat tersebut.
            </p>
          </section>
          <section>
            <h3>JavaScript</h3>
            <img src="images/js.jpg" alt="JS" class="content-image" />
            <p>
              JavaScript adalah bahasa pemrograman tingkat tinggi yang digunakan untuk mengembangkan aplikasi web interaktif. JavaScript dapat digunakan untuk menambahkan interaksi dan fungsi ke halaman web seperti validasi form, animasi,
              efek transisi, dan manipulasi DOM. JavaScript dapat dijalankan pada browser web atau pada server menggunakan platform seperti Node.js. Saat dijalankan di browser, JavaScript dapat diintegrasikan dengan HTML dan CSS untuk
              membuat tampilan dan interaksi yang dinamis pada halaman web.
            </p>
          </section>
